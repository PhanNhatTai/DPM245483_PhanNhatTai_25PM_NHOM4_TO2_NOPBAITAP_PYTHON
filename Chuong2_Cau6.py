#Trình bày ý nghĩa toán tử /, //, %, **, and, or, is.
# / : toán tử chia
# // :toán tử chia nhưng trả về phần nguyên bỏ qua phần thập phân
# % :chia lấy phần dư
# ** : toán tử lũy thừa cơ số mũ
# and : toán tử và
# or : toán tử hoặc
# is : toán tử kiểm tra xem hai biến có tham chiếu đến cùng 1 đối tượng hay không
